меню_трапезної = {
    яєчня: 2,
    деруни: 3,
    повидло: 1,
    ковбаска: 2,
    оладки: (1..3)
}

меню_трапезної.keys.each do |страва|
  puts "Сьогодні в меню: #{страва}!"
end

меню_трапезної.each do |страва, ціна|
  puts "#{страва} всього лиш по #{ціна} грн!"
end

puts "А ось ціни на оладки, залежно від кількості повидла:"
меню_трапезної[:оладки].to_a.each do |ціна|
  puts "#{ціна} грн"
end