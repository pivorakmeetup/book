require './зафарбувати'

class Корм
  def подати
    puts 'Корм подано!'
  end
end

class КормДляПанд < Корм
  include Зафарбувати
  attr_reader :калорії

  КАЛОРІЙ_НА_ОДНУ_ПОРЦІЮ = 1000

  def initialize
    @калорії = КАЛОРІЙ_НА_ОДНУ_ПОРЦІЮ
  end

  def подати
    puts 'Одну гаряченьку порцію пандо-корму подано!'
  end

  def аналізувати
    puts "Цей корм має #{@калорії} калорій і #{колір} колір."
  end
end

корм_для_хогарта = КормДляПанд.new
puts корм_для_хогарта.аналізувати