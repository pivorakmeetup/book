def створити_вантажні_доки(доки = 3)
  вантажні_доки = []
  (1..доки).each do |номер|
    назва_файлу = "доки_#{номер}.txt"
    вантажні_доки << назва_файлу
    файл = File.open(назва_файлу, 'w+')
    файл.write("Вантажний док номер #{номер}: до роботи!")
    файл.close
  end
  вантажні_доки
end

def відкрити_вантажні_доки(доки)
  доки.each do |док|
    файл = File.open(док, 'r')
    puts файл.read
    файл.close
  end
end

усі_доки = створити_вантажні_доки(5)
відкрити_вантажні_доки(усі_доки)